package ex_17092024;

public class lab018 {
    public static void main(String[] args) {
        short s = 10;

        char c = 'B';

        int ss = s+c;

        System.out.println(ss);
       // Character Arithmetic:

//        In Java, characters are represented by their ASCII values. The ASCII value of 'B' is 66.
//        Therefore, s + c is equivalent to 10 + 66, which results in 76.
//        Output:
//
//        System.out.println(ss); prints the value of ss, which is 76.
    }
}
